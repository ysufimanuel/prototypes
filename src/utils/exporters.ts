/**
 * Helper: Download file dari string content
 */
function downloadFile(content: string, filename: string, mimeType: string): void {
  const blob = new Blob([content], { type: mimeType + ';charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.download = filename;
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  URL.revokeObjectURL(url);
}

/**
 * Backup seluruh data ke JSON file
 */
export function backupData(data: Record<string, any>): void {
  const jsonStr = JSON.stringify(data, null, 2);
  const filename = `cms-backup-${new Date().toISOString().split('T')[0]}.json`;
  downloadFile(jsonStr, filename, 'application/json');
}

/**
 * Restore data dari JSON file
 */
export function restoreData(file: File): Promise<Record<string, any>> {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = e => {
      try {
        const data = JSON.parse(e.target?.result as string);
        resolve(data);
      } catch {
        reject(new Error('File JSON tidak valid'));
      }
    };
    reader.onerror = () => reject(new Error('Gagal membaca file'));
    reader.readAsText(file);
  });
}
