/**
 * Exporters - Fungsi untuk export data ke berbagai format
 */

/**
 * Convert array of objects ke CSV
 */
export function exportCSV(data: Record<string, any>[], filename: string): void {
  if (!data || data.length === 0) {
    alert('Tidak ada data untuk diexport');
    return;
  }

  const headers = Object.keys(data[0]);
  const csvContent = [
    headers.join(','),
    ...data.map(row =>
      headers
        .map(h => {
          const val = row[h];
          if (val === null || val === undefined) return '';
          const str = String(val).replace(/"/g, '""');
          if (str.includes(',') || str.includes('\n') || str.includes('"')) {
            return `"${str}"`;
          }
          return str;
        })
        .join(',')
    ),
  ].join('\n');

  downloadFile(csvContent, `${filename}.csv`, 'text/csv');
}

/**
 * Export ke format XLS (HTML table yang bisa dibuka Excel)
 */
export function exportXLS(data: Record<string, any>[], filename: string): void {
  if (!data || data.length === 0) {
    alert('Tidak ada data untuk diexport');
    return;
  }

  const headers = Object.keys(data[0]);
  let html = '<table border="1"><thead><tr>';
  headers.forEach(h => {
    html += `<th>${h}</th>`;
  });
  html += '</tr></thead><tbody>';

  data.forEach(row => {
    html += '<tr>';
    headers.forEach(h => {
      const val = row[h];
      html += `<td>${val !== null && val !== undefined ? val : ''}</td>`;
    });
    html += '</tr>';
  });

  html += '</tbody></table>';

  downloadFile(html, `${filename}.xls`, 'application/vnd.ms-excel');
}

/**
 * Export ke format TXT
 */
export function exportTXT(data: Record<string, any>[], filename: string): void {
  if (!data || data.length === 0) {
    alert('Tidak ada data untuk diexport');
    return;
  }

  const headers = Object.keys(data[0]);
  let text = headers.join('\t') + '\n';

  data.forEach(row => {
    text += headers.map(h => row[h] ?? '').join('\t') + '\n';
  });

  downloadFile(text, `${filename}.txt`, 'text/plain');
}

/**
 * Export ke format PDF menggunakan window.print
 */
export function exportPDF(data: Record<string, any>[], title: string): void {
  if (!data || data.length === 0) {
    alert('Tidak ada data untuk diexport');
    return;
  }

  const headers = Object.keys(data[0]);
  let html = `
    <html>
    <head>
      <title>${title}</title>
      <style>
        body { font-family: Arial, sans-serif; padding: 20px; }
        h1 { text-align: center; color: #333; }
        table { width: 100%; border-collapse: collapse; margin-top: 20px; }
        th, td { border: 1px solid #ddd; padding: 8px; text-align: left; }
        th { background-color: #f2f2f2; font-weight: bold; }
        tr:nth-child(even) { background-color: #f9f9f9; }
        .footer { text-align: center; margin-top: 30px; font-size: 12px; color: #666; }
      </style>
    </head>
    <body>
      <h1>${title}</h1>
      <p>Tanggal: ${new Date().toLocaleDateString('id-ID')}</p>
      <table>
        <thead>
          <tr>
            ${headers.map(h => `<th>${h}</th>`).join('')}
          </tr>
        </thead>
        <tbody>
          ${data
            .map(
              row =>
                `<tr>${headers.map(h => `<td>${row[h] ?? ''}</td>`).join('')}</tr>`
            )
            .join('')}
        </tbody>
      </table>
      <div class="footer">
        Generated by Church Management System
      </div>
    </body>
    </html>
  `;

  const printWindow = window.open('', '_blank');
  if (printWindow) {
    printWindow.document.write(html);
    printWindow.document.close();
    printWindow.focus();
    setTimeout(() => {
      printWindow.print();
      printWindow.close();
    }, 500);
  }
}

